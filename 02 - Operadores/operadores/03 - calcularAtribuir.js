// (+=) -> sinal matemático antes de um sinal de atribuição
// "soma com você mesmo + 4"
let preco = 10
console.log(preco)
preco += 5 // preco = preco + 5
console.log(preco)
preco -= 3 // preco = preco - 3
console.log(preco)

//  =  -> Atribuição                 - let x = 5;
// +=  -> Adição e atribuição        - let num = 10; num += 2;
// -=  -> Subtração e atribuição     - let num = 10; num -= 3;
// *=  -> Multiplicação e atribuição - let num = 5; num *= 4;
// /=  -> Divisão e atribuição       - let num = 10; num /= 2;
// %=  -> Módulo e atribuição        - let num = 10; num /= 2;